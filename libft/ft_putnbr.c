#include <unistd.h>

void	ft_putnbr(int n)
{
	int unsigned	i;
	char			c;

	if (n < 0)
	{
		c = '-';
		write(1, &c, 1);
		i = -n;
	}
	else
		i = n;
	if (i >= 10)
		ft_putnbr(i / 10);
	c = i % 10 + '0';
	write(1, &c, 1);
}
